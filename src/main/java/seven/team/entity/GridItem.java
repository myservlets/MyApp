package seven.team.entity;

public class GridItem {
    private int gridItemIcon;
    private String gridItemName;

    public int getGridItemIcon() {
        return gridItemIcon;
    }

    public void setGridItemIcon(int gridItemIcon) {
        this.gridItemIcon = gridItemIcon;
    }

    public String getGridItemName() {
        return gridItemName;
    }

    public void setGridItemName(String gridItemName) {
        this.gridItemName = gridItemName;
    }
}
